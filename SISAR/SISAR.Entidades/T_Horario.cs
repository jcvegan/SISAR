using System;

namespace SISAR.Entidades
{
	public class T_Horario
	{
		#region Fields

		private int id_Horario;
		private int id_Empleado;
		private string dia;
		private DateTime hora_Inicio;
		private int duracion;

		#endregion

		#region Constructors

		/// <summary>
		/// Initializes a new instance of the T_Horario class.
		/// </summary>
		public T_Horario()
		{
		}

		/// <summary>
		/// Initializes a new instance of the T_Horario class.
		/// </summary>
		public T_Horario(int id_Empleado, string dia, DateTime hora_Inicio, int duracion)
		{
			this.id_Empleado = id_Empleado;
			this.dia = dia;
			this.hora_Inicio = hora_Inicio;
			this.duracion = duracion;
		}

		/// <summary>
		/// Initializes a new instance of the T_Horario class.
		/// </summary>
		public T_Horario(int id_Horario, int id_Empleado, string dia, DateTime hora_Inicio, int duracion)
		{
			this.id_Horario = id_Horario;
			this.id_Empleado = id_Empleado;
			this.dia = dia;
			this.hora_Inicio = hora_Inicio;
			this.duracion = duracion;
		}

		#endregion

		#region Properties
		/// <summary>
		/// Gets or sets the Id_Horario value.
		/// </summary>
		public virtual int Id_Horario
		{
			get { return id_Horario; }
			set { id_Horario = value; }
		}

		/// <summary>
		/// Gets or sets the Id_Empleado value.
		/// </summary>
		public virtual int Id_Empleado
		{
			get { return id_Empleado; }
			set { id_Empleado = value; }
		}

		/// <summary>
		/// Gets or sets the Dia value.
		/// </summary>
		public virtual string Dia
		{
			get { return dia; }
			set { dia = value; }
		}

		/// <summary>
		/// Gets or sets the Hora_Inicio value.
		/// </summary>
		public virtual DateTime Hora_Inicio
		{
			get { return hora_Inicio; }
			set { hora_Inicio = value; }
		}

		/// <summary>
		/// Gets or sets the Duracion value.
		/// </summary>
		public virtual int Duracion
		{
			get { return duracion; }
			set { duracion = value; }
		}

		#endregion
	}
}
