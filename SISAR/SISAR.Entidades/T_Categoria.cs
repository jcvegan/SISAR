using System;

namespace SISAR.Entidades
{
	public class T_Categoria
	{
		#region Fields

		private int id_Categoria;
		private string nombre;
		private string descripcion;
		private int categoria_Padre;

		#endregion

		#region Constructors

		/// <summary>
		/// Initializes a new instance of the T_Categoria class.
		/// </summary>
		public T_Categoria()
		{
		}

		/// <summary>
		/// Initializes a new instance of the T_Categoria class.
		/// </summary>
		public T_Categoria(string nombre, string descripcion, int categoria_Padre)
		{
			this.nombre = nombre;
			this.descripcion = descripcion;
			this.categoria_Padre = categoria_Padre;
		}

		/// <summary>
		/// Initializes a new instance of the T_Categoria class.
		/// </summary>
		public T_Categoria(int id_Categoria, string nombre, string descripcion, int categoria_Padre)
		{
			this.id_Categoria = id_Categoria;
			this.nombre = nombre;
			this.descripcion = descripcion;
			this.categoria_Padre = categoria_Padre;
		}

		#endregion

		#region Properties
		/// <summary>
		/// Gets or sets the Id_Categoria value.
		/// </summary>
		public virtual int Id_Categoria
		{
			get { return id_Categoria; }
			set { id_Categoria = value; }
		}

		/// <summary>
		/// Gets or sets the Nombre value.
		/// </summary>
		public virtual string Nombre
		{
			get { return nombre; }
			set { nombre = value; }
		}

		/// <summary>
		/// Gets or sets the Descripcion value.
		/// </summary>
		public virtual string Descripcion
		{
			get { return descripcion; }
			set { descripcion = value; }
		}

		/// <summary>
		/// Gets or sets the Categoria_Padre value.
		/// </summary>
		public virtual int Categoria_Padre
		{
			get { return categoria_Padre; }
			set { categoria_Padre = value; }
		}

		#endregion
	}
}
