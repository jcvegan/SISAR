using System;

namespace SISAR.Entidades
{
	public class T_Perfil
	{
		#region Fields

		private int id_Perfil;
		private string nombre;
		private string descripcion;

		#endregion

		#region Constructors

		/// <summary>
		/// Initializes a new instance of the T_Perfil class.
		/// </summary>
		public T_Perfil()
		{
		}

		/// <summary>
		/// Initializes a new instance of the T_Perfil class.
		/// </summary>
		public T_Perfil(string nombre, string descripcion)
		{
			this.nombre = nombre;
			this.descripcion = descripcion;
		}

		/// <summary>
		/// Initializes a new instance of the T_Perfil class.
		/// </summary>
		public T_Perfil(int id_Perfil, string nombre, string descripcion)
		{
			this.id_Perfil = id_Perfil;
			this.nombre = nombre;
			this.descripcion = descripcion;
		}

		#endregion

		#region Properties
		/// <summary>
		/// Gets or sets the Id_Perfil value.
		/// </summary>
		public virtual int Id_Perfil
		{
			get { return id_Perfil; }
			set { id_Perfil = value; }
		}

		/// <summary>
		/// Gets or sets the Nombre value.
		/// </summary>
		public virtual string Nombre
		{
			get { return nombre; }
			set { nombre = value; }
		}

		/// <summary>
		/// Gets or sets the Descripcion value.
		/// </summary>
		public virtual string Descripcion
		{
			get { return descripcion; }
			set { descripcion = value; }
		}

		#endregion
	}
}
